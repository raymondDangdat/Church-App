package comq.example.raymond.Model;

public class MemberModel {
    public String name, status, profilePic, phone, gender, email, address;

    public MemberModel() {
    }

    public MemberModel(String name, String status, String profilePic, String phone, String gender, String email, String address) {
        this.name = name;
        this.status = status;
        this.profilePic = profilePic;
        this.phone = phone;
        this.gender = gender;
        this.email = email;
        this.address = address;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getProfilePic() {
        return profilePic;
    }

    public void setProfilePic(String profilePic) {
        this.profilePic = profilePic;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }


}
